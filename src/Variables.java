public class Variables {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        System.out.println(a+b);

        // primitive datatypes
        int x = 1; // 4 byte
        float y = 45.67f; // 4byte
        double d = 332.2323; // 8 byte
        long l = 324243224l; // 8 bye

        char c = 'c'; // 2 byte
        boolean t = true; // 1 byte
    }
}
